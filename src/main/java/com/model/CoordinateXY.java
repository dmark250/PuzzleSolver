package main.java.com.model;

public class CoordinateXY {
    private int x;
    private int y;

    public CoordinateXY(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int xValue) {
        this.x = xValue;
    }

    public void setY(int yValue) {
        this.y = yValue;
    }

}
